    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        int choice = Integer.parseInt(ClerkChoiceTextField.getText());


        if (choice == 1) {
            String title = TitleTextField.getText();

            String PrintingMessage = "No Record found";


            ArrayList<Books> SelectedBooks = Obj.searchBookByTitle(title);

            if (SelectedBooks.isEmpty() == false) {
                PrintingMessage = " ";
                for (int counter = 0; counter < SelectedBooks.size(); counter++) {
                    Books B = SelectedBooks.get(counter);

                    PrintingMessage = PrintingMessage + "   " + B.getBookId() + "  " + B.getTitle() + "    " + B.getAuthor() + "\n";
                }

            }

            ResultMsgTextField.setText(PrintingMessage);
        } else if (choice == 2) {

            String author = AuthorTextField.getText();

            String PrintingMessage = "No Record found";

            ArrayList<Books> SelectedBooks = Obj.searchBookByAuthor(author);

            if (SelectedBooks.isEmpty() == false) {
                PrintingMessage = " ";
                for (int counter = 0; counter < SelectedBooks.size(); counter++) {
                    Books B = SelectedBooks.get(counter);

                    PrintingMessage = PrintingMessage + "   " + B.getBookId() + "  " + B.getTitle() + "    " + B.getAuthor() + "\n";
                }
            }

            ResultMsgTextField.setText(PrintingMessage);

        }  else if (choice == 3) {
            String studentName = NewNameTextField.getText();
            char gender = NewGenderTextField.getText().charAt(0);
            String BITS_ID = NewBitsIdTextField.getText();
            String password = PasswordTextField.getText();
            boolean result = Obj.addNewStudent(studentName, gender, id, BITS_ID, password);
            if (result == true) {

                ResultMsgTextField.setText("New User added");
            } else {
                ResultMsgTextField.setText("Command Failed");
            }
        } else if (choice == 4) {
            String studentId = IdTextFieldStudent.getText();
            String Info = UpdatedInfoTextFieldStudent.getText();
            boolean result = false;
            if (UpdateStudentNameRadioButton.isSelected()) {
                result = Obj.updateStudentInformation(Info, 1, id, studentId);

            } else if (UpdateStudentGenderRadioButton.isSelected()) {
                result = Obj.updateStudentInformation(Info, 2, id, studentId);

            } else if (UpdateStudentBitsIdRadioButton.isSelected()){
                result = Obj.updateStudentInformation(Info, 3, id, studentId);
            }


            if (result == false) {
                ResultMsgTextField.setText("Command Failed");
            } else {
                ResultMsgTextField.setText("Updated Successfully");
            }

        } else if (choice == 5) {

            String title = TitleTextFieldCreateBook.getText();
            String author = AuthorTextFieldCreateBook.getText();
            int quantity = Integer.parseInt(QuantityTextFieldCreateBook.getText());

            boolean result = Obj.addNewBook(title,author, quantity, this.id);

            if (result == true){
                ResultMsgTextField.setText("Book Successfully added to the Library");
            } else {
                ResultMsgTextField.setText("Operation Failed");
            }

        }  else if (choice == 6) {
            int bookId = Integer.parseInt(UpdateBookInfoID.getText());
            String Info = UpdateBookInfoUpdatedInfo.getText();
            boolean result = false;
            int option = 0;
            // Improve error handling
            if (updateBookInfoTitle.isSelected()) {
                option = 1;
                Obj.updateBookInformation(bookId, this.id, Info, option);

            } else if (UpdateBookInfoAuthor.isSelected()) {
                option = 2;
                Obj.updateBookInformation(bookId, this.id, Info, option);

            } else if (UpdateBookInfoQuantity.isSelected()){
                option = 3;
                Obj.updateBookInformation(bookId, this.id, Info, option);
            }

            ResultMsgTextField.setText("Updated Successfully");

        } else if (choice == 7) {
            int bookId = Integer.parseInt(DeleteBookWithId.getText());
            boolean result = Obj.deleteBook(bookId, this.id);

            if (result == true){
                ResultMsgTextField.setText("Book Deleted Successfully");
            } else {
                ResultMsgTextField.setText("Operation Failed");
            }

        }else {
            ResultMsgTextField.setText("Invalid Command");
        }
    }//GEN-LAST:event_jButton1MouseClicked

